package Java5;

public class LogicalOPerator {
    public static void main(String[] args) {

        boolean box=false;
        System.out.println(box);
        System.out.println(!box);
        System.out.println(box); //! reverse condition

        if(box){
            System.out.println("inside if part");
        }else {
            System.out.println("inside else part");
        }

        if (!box){
            System.out.println("inside if part");
        }else {
            System.out.println("inside else part");
        }








    }



}
