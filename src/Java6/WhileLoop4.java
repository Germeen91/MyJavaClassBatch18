package Java6;

public class WhileLoop4 {
    public static void main(String[] args) {
        int x = 1;
        //first conditions is checked if condition is true we enter the loop


        while (x <= 5) { // first instruction is if statement if it is true print statement is executed
            if (x != 2) {

                System.out.println(x);
            }

                ++x;    //condition to stop printing
            }

        }
                // ++ it is called increment
                // -- it is called decrement
    }
