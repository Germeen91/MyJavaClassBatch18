package Java6;

public class Task3 {
    public static void main(String[] args) {

        // print even numbers from 20 to 1(using 2 different loops)

        for (int s=20;s>=1;s=s-2){
            System.out.print(s+" ");
        }
        System.out.println(); // to make space

        int d=20;
        while (d>=1){
            System.out.print(d+" ");
            d=d-2;
        }



    }
}
