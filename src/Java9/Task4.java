package Java9;

public class Task4 {
    public static void main(String[] args) {



        // the highest number


        int [] num={5,4,8};


        int highest=num[0];

        for (int i=0; i< num.length; i++){

            if (num[i]>highest){

                highest=num[i];
            }

        }

        System.out.println(highest);










    }
}
