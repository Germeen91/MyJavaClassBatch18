package Java2;

public class concat7 {
    public static void main(String[] args) {

        int n1=100;
        int n2=n1+10;
        System.out.println(n2);


        int num=100; // java stored 100

        num=num+20; // update 100+20 because we stored 100
        System.out.println(num);
        num=0; // update the whole  from 100 to 0
        System.out.println(num);


    }
}
