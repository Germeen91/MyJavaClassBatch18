package Java2;

public class concat5 {

    public static void main(String[] args) {

        int n1=10;
        int n2=2;
        System.out.println(n1/n2);

        double n3=10;    //decimal number
        double n4=3;
        System.out.println(n3/n4);

        int n5=10;       //integer number
        int n6=3;
        System.out.println(n5/n6);

        System.out.println(n5%n6);  //3+3+3-10 the left after divide is 1 //always the big number% the small number

        // it is called n5 modded by n6 and the remainder (the leftover) is 1






    }
}
