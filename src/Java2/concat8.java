package Java2;

public class concat8 {
    public static void main(String[] args) {


         int num=100;
         num+=10;
        System.out.println(num);

        int num2=100;
        num2/=10;
        System.out.println(num2);

        int g=3;
        g+=2;          //shorthand (to put 2 extra)
        System.out.println(g);

        int c=6;
        c-=1;    // we already stored 6 and need to subtract 1 form it

        System.out.println(c);



    }
}
