package Java7;

public class NestedLoop4 {
    public static void main(String[] args) {



        for (int p=0;p<=9;p++){

            for (int l=0; l<=9; l++){
                for (int m=0; m<=9; m++){
                    for (int v=0; v<=9; v++){
                        System.out.println(p + " "+l+" "+m+" "+v);
                    }
                }
            }




        }




    }
}
