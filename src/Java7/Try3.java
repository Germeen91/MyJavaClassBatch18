package Java7;

public class Try3 {
    public static void main(String[] args) {
// print table 1 to 10


        for (int i=1;i<=10;++i){
            for (int l=1;l<=10;++l){
                System.out.println(i+" * "+l+" = "+(i*l));
            }
            System.out.println(".............");



        }









    }
}
